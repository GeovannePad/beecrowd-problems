n100 = int(N // 100)
N = N - (n100 * 100)
n50 = int(N // 50)
N = N - (n50 * 50)
n20 = int(N // 20)
N = N - (n20 * 20)
n10 = int(N // 10)
N = N - (n10 * 10)
n5 = int(N // 5)
N = N - (n5 * 5)
n2 = int(N // 2)
N = N - (n2 * 2)

