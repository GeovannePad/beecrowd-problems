N = N - (c1 * 1)
c050 = int(N // 0.50)
N = N - (c050 * 0.50)
c025 = int(N // 0.25)
N = N - (c025 * 0.25)
c010 = int(N // 0.10)
N = N - (c010 * 0.10)
